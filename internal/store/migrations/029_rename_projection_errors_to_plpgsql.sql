-- Operator-trap cleanup: rename `projection_errors` to make its
-- narrowed scope visible everywhere it appears.
--
-- Before this migration, operators inspecting `SELECT * FROM
-- projection_errors` reasonably assumed the table captured EVERY
-- projector failure. After tracker #107 ported eleven projectors
-- to Go listeners (which emit slog.Warn instead of writing to this
-- table), the assumption silently became wrong: an empty table now
-- means "no PL/pgSQL projector errors", not "no projector errors".
-- Renaming + a COMMENT pins that scope at every observation point
-- (psql `\d`, audit dashboards, monitoring queries).
--
-- Also drops the unused `ListProjectionErrors` and
-- `CountProjectionErrors` sqlc queries — they were scaffolded but
-- nothing in production code calls them, so they only contributed
-- to the operator confusion ("there are queries to read it, so it
-- must be the canonical surface").
--
-- See manchtools/power-manage-server#107.

-- +goose Up

ALTER TABLE projection_errors RENAME TO plpgsql_projection_errors;

COMMENT ON TABLE plpgsql_projection_errors IS
    'Captures errors raised inside PL/pgSQL projector functions invoked via the project_event() trigger. Go projectors registered through projectors.WireAll do NOT write here — they emit slog.Warn instead. An empty table means "no PL/pgSQL projector errors", not "no projector errors of any kind". Inspect slog output for the Go side.';

